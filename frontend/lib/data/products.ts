export function getFamilyBySlug(sectorSlug: string, familySlug: string) {
  const sector = getSectorBySlug(sectorSlug);
  return sector?.families.find((f) => f.slug === familySlug);
}

export function getProductBySlug(sectorSlug: string, familySlug: string, productSlug: string) {
  const family = getFamilyBySlug(sectorSlug, familySlug);
  return family?.products.find((p) => p.slug === productSlug);
}
